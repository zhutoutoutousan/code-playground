class Person {
	constructor(r, c) {
		this._r = r;
		this._c = c;

		this._head = choose(head);
		this._mouth = choose(mouth);
		this._pants = choose(pants);
		this._shirt = choose(shirt);
		this._hair = choose(hair);
		this._hairExtensions = choose(hairExtensions);
		this._hands = choose(hands);
		this._eyes = choose(eyes);

		this._skinColor = skinColor();
		this._mouthColor = mouthColor();
		this._eyeColor = eyeColor();
		this._shirtColor = clothesColor();
		this._pantsColor = clothesColor();
		this._hairColor = hairColor();

		this._isHeadFlip = Math.random() < 0.5;
		this._isMouthFlip = Math.random() < 0.5;
		this._isPantsFlip = Math.random() < 0.5;
		this._isShirtFlip = Math.random() < 0.5;
		this._isHairFlip = Math.random() < 0.5;
		this._isHairExtensionsFlip = Math.random() < 0.5;
		this._isHandsFlip = Math.random() < 0.5;
		this._isEyesFlip = Math.random() < 0.5;

		this._mouthLevel = ((Math.random() * 2) | 0) * pixelSize;
		this._eyeLevel = ((Math.random() * 2) | 0) * pixelSize;
		this._bodyLevel = ((Math.random() * 2) | 0) * pixelSize;
		this._handsLevel = ((Math.random() * 2) | 0) * pixelSize;

		this._isFacingForwards = Math.random() < 0.5;
	}
	_renderClothes() {
		if (this._isFacingForwards) {
			draw(this._r, this._c, this._shirt, this._shirtColor, this._isShirtFlip);
			draw(this._r, this._c, this._pants, this._pantsColor, this._isPantsFlip);
		} else {
			draw(this._r, this._c, this._pants, this._pantsColor, this._isPantsFlip);
			draw(this._r, this._c, this._shirt, this._shirtColor, this._isShirtFlip);
		}
	}
	_renderHead() {
		draw(
			this._r + this._bodyLevel,
			this._c,
			this._head,
			this._skinColor,
			this._isFacingForwards ? this._isHeadFlip : !this._isHeadFlip
		);
		draw(
			this._r + this._bodyLevel,
			this._c,
			this._hair,
			this._hairColor,
			this._isFacingForwards ? this._isHairFlip : !this._isHairFlip
		);
		if (this._isFacingForwards) {
			draw(
				this._r + this._bodyLevel + this._mouthLevel,
				this._c,
				this._mouth,
				this._mouthColor,
				this._isMouthFlip
			);
			draw(
				this._r + this._bodyLevel + this._eyeLevel,
				this._c,
				this._eyes,
				this._eyeColor,
				this._isEyesFlip
			);
		}
	}
	_doFlips() {
		if (Math.random() < 0.05) this._isFacingForwards = !this._isFacingForwards;
		this._isPantsFlip = !this._isPantsFlip;
		this._isHandsFlip = !this._isHandsFlip;
		this._isShirtFlip = !this._isShirtFlip;
		if (Math.random() < 0.05) {
			this._isHeadFlip = !this._isHeadFlip;
			this._isEyesFlip = !this._isEyesFlip;
			this._isMouthFlip = !this._isMouthFlip;
			this._isHairFlip = !this._isHairFlip;
		}
	}
	_renderBody() {
		if (this._isFacingForwards) {
			this._renderClothes();
			this._renderHead();
		} else {
			this._renderHead();
			this._renderClothes();
		}
	}
	_renderHands() {
		draw(
			this._r + this._handsLevel,
			this._c,
			this._hands,
			this._skinColor,
			this._isHandsFlip
		);
	}
	_renderHairExtensions() {
		draw(
			this._r + this._bodyLevel,
			this._c,
			this._hairExtensions,
			this._hairColor,
			this._isFacingForwards
				? this._isHairExtensionsFlip
				: !this._isHairExtensionsFlip
		);
	}
	render() {
		this._doFlips();
		draw(this._r + pixelSize, this._c, dropShadow, "hsla(0,0%,0%,0.05)");
		if (this._isFacingForwards) {
			this._renderHairExtensions();
			this._renderBody();
			this._renderHands();
		} else {
			this._renderHands();
			this._renderBody();
			this._renderHairExtensions();
		}
	}
}
function draw(r, c, G, color, isFlip) {
	const rs = G.length;
	const cs = G[0].length;
	for (let rr = 0; rr < rs; ++rr) {
		for (let cc = 0; cc < cs; ++cc) {
			ctx.beginPath();
			ctx.fillStyle = color;
			if (G[rr][cc] !== 0) {
				const addC = isFlip ? cs - cc - 1 : cc;
				ctx.rect(c + addC * pixelSize, r + rr * pixelSize, pixelSize, pixelSize);
			}
			ctx.fill();
		}
	}
}
function choose(A) {
	return A[(Math.random() * A.length) | 0];
}
function render() {
	ctx.clearRect(0, 0, canvas.width, canvas.height);
	for (let p of people) p.render();
}
function init() {
	const cs = Math.ceil(canvas.width / pWidth / pixelSize);
	const rs = Math.ceil(canvas.height / pHeight / pixelSize);
	people = [];
	for (let r = 0; r < rs; ++r) {
		for (let c = 0; c < cs; ++c) {
			people.push(new Person(r * pHeight * pixelSize, c * pWidth * pixelSize));
		}
	}
}
let time = 0;
let lastTime = 0;
function loop() {
	time = Date.now();
	if (time - lastTime > 1000 / fps) {
		render();
		lastTime = time;
	}
	requestAnimationFrame(loop);
}
function resize() {
	canvas.width = window.innerWidth * devicePixelRatio;
	canvas.height = window.innerHeight * devicePixelRatio;
	init();
}
window.onresize = resize;
window.onclick = init;
resize();
loop();
